# we cannot use any number or capital word at the starting of the variable instead of space we can use underscore(_)
# some assignment operators are:
# = to assign any value to variable
# += to add some value to variable
# -= to subtract any value from the variable
# Example
ex = 32
print('Example variable is equal to:', ex)
ex += 1
print('Now example variable is equal to:', ex)
ex -= 1
print('Now example variable is equal to:', ex)
