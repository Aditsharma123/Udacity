# + for addition
# - for subtraction
# * for multiplication
# / for division
# ** for exponentiation
# == for equality
# = to change value of variable or other
# % for calculating the remainder after division(modulo or modulus)
# // for integer division(rounds down the correct answer to an integer other than a point answer)


# example for addition
print('Example of addition: 2+5=', 2 + 5)

# example of subtraction
print('Example of subtraction: 5-2=', 5 - 2)

# example of multiplication
print('Example of multiplication: 2*5=', 2 * 5)

# example for division
print('Example of division: 2/5=', 2 / 5)

# example for integer division
print('Example of integer division: 11//2=', 11 // 5)

# example for exponentiation
print('Example of exponentiation: 11**2=', 11 ** 2)

# example for equality
num = 1
if num == 1:
    print('Example of equality')

# example for modulo
print('Example of modulo: 11%2=', 11 % 2)
